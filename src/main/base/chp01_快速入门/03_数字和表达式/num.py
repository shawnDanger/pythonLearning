# 加法
print("加法:")
print(2 + 2)
# 除法 保留一位小数
print("除法:")
print(1 / 2)
# 除法 使用所使用的类型当做返回类型,如整数
print(1 // 2)
print(3 // 2)
# 除法 包含浮点数的除法结果为浮点数
print(2 // 1.0)
print(2.0 // 1.0)

# 求模(求余)
print("求模(求余):")
print(1 % 2)
# 求模(求余) 与前者等价
x = 1
y = 2
print(x - ((x // y) * y))
# 乘方
print("乘方:")
print(2 ** 3)
print(-3 ** 2)
print((-3) ** 2)
# 十六进制,八进制,二进制
print(0xAF)
print(0o10)
print(0b101)
